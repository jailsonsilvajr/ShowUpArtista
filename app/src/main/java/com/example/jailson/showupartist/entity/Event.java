package com.example.jailson.showupartist.entity;

public class Event {

    private String name;
    private String date;

    public Event(){}

    public String getName(){

        return this.name;
    }

    public void setName(String name){

        this.name = name;
    }

    public String getDate(){

        return this.date;
    }

    public void setDate(String date){

        this.date = date;
    }
}
